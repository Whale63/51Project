#include "delay.h"

//========================================
// 微秒延时 (12MHz晶振)
//========================================
void delay_us(uint16 us)
{
    while(us--)
    {
        _nop_(); _nop_(); _nop_(); _nop_();
        _nop_(); _nop_(); _nop_(); _nop_();
    }
}

//========================================
// 毫秒延时
//========================================
void delay_ms(uint16 ms)
{
    uint16 i, j;
    for(i = 0; i < ms; i++)
        for(j = 0; j < 120; j++);  // 约1ms @12MHz
}
